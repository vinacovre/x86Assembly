	SECTION .data
var1:	db	0x7F		; 01111111B = 127
var2:	db	80h			; 10000000B	= -128

	SECTION .text

global _start

_start:

	; 2'S COMPLEMENT LOGIC...
	; number   -->  invert  --> add 1

	;BLOCK 1
	mov	eax,[var2]
	sub	eax,[var1]
	;
	; 10000000 --> 01111111 --> 10000000
	; -
	; 01111111 --> 10000000 --> 10000001
	; =
	; 11111111 --> 00000000 --> 00000001 = 1

	;BLOCK 2
	; mov	eax,[var1]
	; sub 	eax,[var2]
	;
	; 01111111 --> 10000000 --> 10000001
	; -
	; 10000000 --> 01111111 --> 10000000
	; =
	; 00000001 --> 11111110 --> 11111111 = 255

;****************************************************

	mov		ebx,eax
